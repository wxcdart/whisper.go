	MOVQ f+8(FP), BX

	VPXOR Y0, Y0, Y0

	MOVQ 0(AX), X1
	VPMOVSXBD X1, Y1
	VCVTDQ2PS Y1, Y1
	VMOVUPS 0(BX), Y2
	VMULPS Y2, Y1, Y1
	VADDPS Y1, Y0, Y0

	MOVQ 8(AX), X1
	VPMOVSXBD X1, Y1
	VCVTDQ2PS Y1, Y1
	VMOVUPS 32(BX), Y2
	VMULPS Y2, Y1, Y1
	VADDPS Y1, Y0, Y0

	MOVQ 16(AX), X1
	VPMOVSXBD X1, Y1
	VCVTDQ2PS Y1, Y1
	VMOVUPS 64(BX), Y2
	VMULPS Y2, Y1, Y1
	VADDPS Y1, Y0, Y0

	MOVQ 24(AX), X1
	VPMOVSXBD X1, Y1
	VCVTDQ2PS Y1, Y1
	VMOVUPS 96(BX), Y2
	VMULPS Y2, Y1, Y1
	VADDPS Y1, Y0, Y0

	VEXTRACTF128 $1, Y0, X1
	VADDPS X1, X0, X0
	VHADDPS X0, X0, X0
	VHADDPS X0, X0, X0

	MOVSS X0, ret+16(FP)
	VZEROUPPER
	RET
